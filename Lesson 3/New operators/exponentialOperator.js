
// Math.pow(x,y) operator => returns x^y

let num = Math.pow(5,2); // => returns 5^2

console.log(num); // output: 25


// ES7 " ** " operator does the same as Math.pow(), but with less code

let x = 5 ** 2; 

console.log(x); // output: 25


// example 2:

let y = 10;

let z = y ** 5; // 10 * 10 * 10 * 10 * 10

console.log(z); // output: 100000


